def number_to_words(n):
    # Словари для преобразования чисел в слова
    units = [
        'ноль', 'один', 'два', 'три', 'четыре', 'пять', 'шесть', 'семь', 'восемь', 'девять',
        'десять', 'одиннадцать', 'двенадцать', 'тринадцать', 'четырнадцать', 'пятнадцать', 'шестнадцать',
        'семнадцать', 'восемнадцать', 'девятнадцать'
    ]
    tens = [
        '', '', 'двадцать', 'тридцать', 'сорок', 'пятьдесят', 'шестьдесят', 'семьдесят', 'восемьдесят', 'девяносто'
    ]
    hundreds = [
        '', 'сто', 'двести', 'триста', 'четыреста', 'пятьсот', 'шестьсот', 'семьсот', 'восемьсот', 'девятьсот'
    ]
    thousands = [
        '', 'одна тысяча', 'две тысячи', 'три тысячи', 'четыре тысячи', 'пять тысяч', 'шесть тысяч', 'семь тысяч',
        'восемь тысяч', 'девять тысяч'
    ]

    # Проверка на ноль
    if n == 0:
        return units[0] + ' рублей'

    if n > 9999:
        return "Ошибка, число больше 9999"

    # Преобразование числа в слова
    result = ''

    if n // 1000 > 0:
        result += thousands[n // 1000] + ' '
        n %= 1000
    if n // 100 > 0:
        result += hundreds[n // 100] + ' '
        n %= 100
    if n // 10 > 1:
        result += tens[n // 10] + ' '
        n %= 10
    if n > 0:
        result += units[n] + ' '

    # Добавление приписки рубля, рублей или рубль
    if n % 100 in range(11, 20):
        result += 'рублей'
    else:
        if n % 10 == 1:
            result += 'рубль'
        elif n % 10 in range(2, 5):
            result += 'рубля'
        else:
            result += 'рублей'

    return result


# Примеры вызова функции
print(number_to_words(0))  # ноль рублей
print(number_to_words(42))  # сорок два рубля
print(number_to_words(123))  # сто двадцать три рубля
print(number_to_words(999))  # девятьсот девяносто девять рублей
print(number_to_words(9762))  # девять тысяч девятьсот девяносто девять рублей
